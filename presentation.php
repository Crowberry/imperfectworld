<h3>"C'est quoi ça?"</h3>

<p>te demanderas-tu. Ici c'est simple, <b>JE</b> suis le dessinateur, <b>TU</b> es l'écrivain. Sur chaque dessin, tu as la possibilité de <b>raconter</b> ce que t'inspire ledit dessin, ou bien lire ce que tes content-porains ont écrit.</p>
<p>"An Imperfect world", c'est Manu. C'est des personnages <b>bizarres</b>, mignons, angoissants/angoissés, droles, légers, chargés, noirs, et blancs.Des trucs et des machins en somme. C'est des traits grossiers, raffinés, <b>effrenés</b>, des êtres pas parfaits mais eh! qui l'est?</p>
<p>C'est pas d'lart, mais plutôt l'artillerie. C'est comme Stupeflip, "prendre des p'tits bouts d'trucs et puis les a assembler <b>ensemble</b>" et matter le résultat tranquille dans ta chambre.</p>
<p>Pareil, on pourrait s'lancer tous ensemble. T'écris le scénar, les histoires de ces dessins aussi loufoques qu'incomplets, et qui sait? ça deviendrait une BD, un jeu-vidéo? Une oeuvre <b>collective</b> de personnages aussi pluriels que TOI, que VOUS.</p>

